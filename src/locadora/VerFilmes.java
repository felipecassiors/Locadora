    public void atualizarFilmeList() {
        filmeListModel.clear();
        
        List<Filme> filmes = filmeDAO.listar();
        for (Filme filme : filmes) {
            filmeListModel.addElement(filme);
        }
        
        filmeList.setModel(filmeListModel);
    }
    
    public void atualizarFilmeList(List<Filme> filmes) {
        filmeListModel.clear();
        
        for (Filme filme : filmes) {
            filmeListModel.addElement(filme);
        }
        
        filmeList.setModel(filmeListModel);
    }

